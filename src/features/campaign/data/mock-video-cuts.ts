export function calculateKpis(cuts: VideoCut[]): VideoCutsKpis {
  const totalDurationSeconds = cuts.reduce((sum, cut) => sum + cut.duration, 0)
  const totalDurationMinutes = Math.round(totalDurationSeconds / 60)

  return {
    totalCuts: cuts.length,
    totalDuration: totalDurationMinutes,
    publishedCount: cuts.filter((c) => c.status === 'published').length,
    reviewCount: cuts.filter((c) => c.status === 'review').length,
    readyCount: cuts.filter((c) => c.status === 'ready').length,
    archivedCount: cuts.filter((c) => c.status === 'archived').length,
  }
}
